package set.set.set.bean;

public class BasicBoardBean {
	private String _id;
	private String id;
	private String title;
	private String cntView;
	private String cmtIds;
	private String actions;
	
	public String get_id() {return _id;}
	public void set_id(String _id) {this._id = _id;}
	public String getId() {return id;}
	public void setId(String id) {this.id = id;}
	public String getTitle() {return title;}
	public void setTitle(String title) {this.title = title;}
	public String getCntView() {return cntView;}
	public void setCntView(String cntView) {this.cntView = cntView;}
	public String getCmtIds() {return cmtIds;}
	public void setCmtIds(String cmtIds) {this.cmtIds = cmtIds;}
	public String getActions() {return actions;}
	public void setActions(String actions) {this.actions = actions;}
}
